void straboscope(unsigned int time, int interval) {
  unsigned long start = millis();
  unsigned long iterStart = millis();
  while (millis() < start + time) {
    while (millis() < iterStart + interval) {
      fillFull();
    }
    iterStart = millis();
    while (millis() < iterStart + interval) {
      clearAll();
    }
    iterStart = millis();
  }
}

